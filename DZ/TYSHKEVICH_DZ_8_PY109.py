def counting_letters(text):
    '''Функция, которая считает гласные и согласные
    в строке'''
    vowels = 0
    consonants = 0
    for c in text.lower():  # Проходим поэлементно по строке
        if c in 'аоуыэеёиюя':
            vowels += 1
        elif c in 'aeiou':
            vowels += 1
        elif c in 'бвгджзйклмнпрстфхцчшщ':
            consonants += 1
        elif c in 'bcdfghjklmnpqrstvwxyz':
            consonants += 1

    return f'гласных: {vowels}, согласных: {consonants}'


string = input('Введите строку: ')
print(counting_letters(string))

