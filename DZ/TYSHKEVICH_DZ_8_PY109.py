def translation_seconds(sec):
    '''Функция, которая переводит часы в дни,
    часы, минуты и секунды'''
    days = sec // (24 * 3600)  # Находим кол-во дней
    sec %= 24 * 3600  # Остаток секунд после определения дней
    hours = sec // 3600  # Находим кол-во часов
    sec %= 3600  # Остаток секунд после определения часов
    minutes = sec // 60  # Находим кол-во минут
    sec %= 60  # Остаток секунд после определения минут
    print(f'Дни:{days} часы:{hours} минуты:{minutes} секунды:{sec}')


secunds = int(input('Введите количество секунд: '))
translation_seconds(secunds)  # Вызываем функцию

