from random import randint

'''Задание №3'''


def list_rand(start, end):
    '''Функция, которая заполняет список
    длиной 10 элементов, случайными числами
    в диапазоне, указанном пользователем'''
    result = [0] * 10  # Заполнем список длиной 10 элем. нулями
    for i in range(len(result)):
        result[i] = randint(start, end)  # меняем i-ый элемент списка на рандомное число
    print(*result)


start = int(input('Введите начало диапазона: '))
end = int(input('Введите конец диапазона: '))

list_rand(start, end)  # Вызываем функцию и передаем позиционные аргументы

