def block_tuple(tuple_):
    '''Функция считает длинну всех слов кортежа'''
    length_words = 0
    for elem in tuple_:  # Пребираем каждый элемент кортежа
        if isinstance(elem, str) and elem.isalpha() and len(elem) > 1:  # Проверка элементов кортежа на слово
            length_words += len(elem)
    return f'Функии передан кортеж {tuple_}, длина всех его слов = {length_words}'


def block_list(list_):
    '''Функция считает кол-во букв и чисел в списке'''
    letters = 0
    numbers = 0
    for elem in list_:  # Пребираем каждый элемент списка
        if isinstance(elem, str) and elem.isalpha() and len(elem) == 1:  # Проверка элементов списка на букву
            letters += 1
        elif isinstance(elem, int):  # Проверка элементов списка на число
            numbers += 1
    return f'Функии передан список {list_}; количество букв в нем = {letters}; количество чисел в нем = {numbers}'


def block_int(num):
    '''Функция считает количество нечетных цифр числа'''
    counter = 0
    x = num
    while x:
        if (x % 10) % 2 != 0:  # Проверка последней цифры на нечетность
            counter += 1
        x //= 10  # Отсекаем последнюю цифру
    return f'Функии передано число {num}, количество нечетных цифр в нем = {counter}'


def block_str(str):
    '''Функция считает количество букв в строке'''
    counter = 0
    for c in str:  # Перебираем строку посимвольно
        if c.isalpha():  # Проверяем, является ли символ буквой
            counter += 1
    return f'Функии передана строка {str}, количество букв в ней = {counter}'


def my_function(obj):
    '''Функция выполняет определенные действия с кортежем/списком/числом/строкой'''
    if isinstance(obj, tuple):  # Проверка на кортеж
        print(block_tuple(obj))
    elif isinstance(obj, list):  # Проверка на список
        print(block_list(obj))
    elif isinstance(obj, int):  # Проверка на число
        print(block_int(obj))
    elif isinstance(obj, str):  # Проверка на строку
        print(block_str(obj))
    else:
        print('Некорректный ввод')


my_function()

